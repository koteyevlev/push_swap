#include <string.h>
#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	index;

	index = 0;
	while (index < n)
	{
		((unsigned char *)dst)[index] = ((unsigned char *)src)[index];
		index++;
		if (((unsigned char *)src)[index - 1] == (unsigned char)c)
			return (&((unsigned char *)dst)[index]);
	}
	return (NULL);
}
